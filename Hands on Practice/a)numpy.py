import numpy as np
array = np.array([1, 2, 3, 4, 5])
added_array = array + 10
subtracted_array = array - 10
multiplied_array = array * 10
print("Original array:", array)
print("Array after adding 10:", added_array)
print("Array after subtracting 10:", subtracted_array)
print("Array after multiplying by 10:", multiplied_array)
